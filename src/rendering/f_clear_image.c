#include "cub3d.h"

void	f_clear_image(t_game *game)
{
	int	y;
	int	x;

	y = -1;
	while (++y < HEIGHT)
	{
		x = -1;
		while (++x < WIDTH)
			f_put_pixel(x, y, 0, game);
	}
}
